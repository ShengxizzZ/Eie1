package com.example.shengxi.eie.beans;

public class Comments {

	private String commentsSid;
	private String commentsId;
	private String mainText;
	private String commentsDate;

	public String getCommentsSid() {
		return commentsSid;
	}

	public void setCommentsSid(String commentsSid) {
		this.commentsSid = commentsSid;
	}

	public String getCommentsId() {
		return commentsId;
	}
	public void setCommentsId(String commentsId) {
		this.commentsId = commentsId;
	}

	public String getMainText() {
		return mainText;
	}
	public void setMainText(String mainText) {
		this.mainText = mainText;
	}
	public String getCommentsDate() {
		return commentsDate;
	}
	public void setCommentsDate(String commentsDate) {
		this.commentsDate = commentsDate;
	}

	

}
